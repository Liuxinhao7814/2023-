void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if(huart->Instance == USART4){
        if(E18_CMD_FLAG != 0)
        {
            E18_RECEIVE_BUFF[RX_NUM] = UART4_temp[0];
            if(E18_RECEIVE_BUFF[0] == 0x55 || E18_RECEIVE_BUFF[0] == 0x4F || E18_RECEIVE_BUFF[0] == 0x1F)
            {
                if(RX_NUM == RX_NUM_LENGTH){
                    RX_NUM = 0;
                    E18_CMD_FLAG = 0;
                } else{
                    RX_NUM++;
                }
            }else{
                RX_NUM = 0;
            }
        }
        HAL_UART_Receive_IT(E18_USART,(uint8_t *)UART4_temp, 1);
    }else if(huart->Instance == USART1){
        HAL_UART_Transmit(&huart1, "Debug\r\n", strlen("Debug\r\n"), 20);
    }
}
